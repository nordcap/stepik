def chunked(arr, n):
    result = []
    temp = []
    for elem in arr:
        temp.append(elem)
        if len(temp) == n:
            result.append(temp)
            temp = []
    if len(temp) > 0:
        result.append(temp)
    return result


arr = input().split()
n = int(input())
print(chunked(arr, n))
